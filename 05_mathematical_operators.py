# MATHEMATICAL OPERATORS
# addition, substraction, multiplication, division

print(2 + 2)
print(3 - 2)
print(3 * 8)
print(35 / 5)

print(10 / 3)   # 3.3333333
print(10 // 3)  # 3
print(10 % 3)   # 1
print( 2 ** 3)  # 8 (2 * 2 * 2)

# USEFUL FUNCTIONS
print(abs(-5))              # 5
print(round(3.7))           # 4
print(round(3.14159, 2))    # 3.14
print(min(3, 4, 2, 6, 8))   # 2
print(max(1, 4, 6, 9, 2))   # 9
print(sum([1, 3, 5]))       # 9

